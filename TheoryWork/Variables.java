class Variables1{
    public static void main(String[] args) {

        /*Variable is a container that stores a value
         *to declare a variable we must specify the datatype
          and give it a name/identifier.
         */

        //declaring a variable:
        int a; //int is data type and a is variable name

        //initializing a variable:
        a= 20; //the value stored in  variable a is 20

        //or declaring and initializing a variable in one line:
        int b = 30;

        System.out.println(a); //printing value of a
        System.out.println(b); //printing value of b
    }
}